let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

// Increment and decrement
console.log(++x); // x incremented before it's returned
console.log(x++); // x is returned, incremented in background

console.log(--x);
console.log(x--);
